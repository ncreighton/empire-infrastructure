# ── Helper functions ───────────────────────────────────────────────────────

def get_meditation(name: str) -> dict | None:
    """Get a meditation framework by key name."""
    return MEDITATION_FRAMEWORKS.get(name.lower().replace(" ", "_"))


def get_meditations_for_intention(intention: str) -> list[dict]:
    """Get meditations matching an intention."""
    intention_lower = intention.lower()
    matches = []
    for key, med in MEDITATION_FRAMEWORKS.items():
        if (intention_lower in med["intention"].lower()
                or any(intention_lower in p.lower() for p in med.get("journal_prompts", []))):
            matches.append({"key": key, **med})
    return matches if matches else [{"key": "grounding", **MEDITATION_FRAMEWORKS["grounding"]}]


def get_all_meditation_keys() -> list[str]:
    """Return all available meditation framework keys."""
    return list(MEDITATION_FRAMEWORKS.keys())
