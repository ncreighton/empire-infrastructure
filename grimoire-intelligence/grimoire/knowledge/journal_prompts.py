# ── Helper functions ───────────────────────────────────────────────────────

def get_daily_prompt(day_of_year: int = 0) -> str:
    """Get a daily prompt, cycling through the list."""
    return DAILY_PROMPTS[day_of_year % len(DAILY_PROMPTS)]


def get_moon_prompts(phase: str) -> list[str]:
    """Get journal prompts for a moon phase."""
    return MOON_PHASE_PROMPTS.get(phase.lower().replace(" ", "_"), DAILY_PROMPTS[:3])


def get_sabbat_prompts(sabbat: str) -> list[str]:
    """Get journal prompts for a sabbat."""
    return SABBAT_PROMPTS.get(sabbat.lower(), [])


def get_shadow_prompt(index: int = 0) -> str:
    """Get a shadow work prompt by index."""
    return SHADOW_WORK_PROMPTS[index % len(SHADOW_WORK_PROMPTS)]


def get_prompts_by_theme(theme: str) -> list[str]:
    """Get prompts matching a theme keyword."""
    theme_lower = theme.lower()
    all_prompts = (
        DAILY_PROMPTS + SHADOW_WORK_PROMPTS + TAROT_JOURNAL_PROMPTS + GRATITUDE_PROMPTS
    )
    matches = [p for p in all_prompts if theme_lower in p.lower()]
    return matches if matches else DAILY_PROMPTS[:5]
